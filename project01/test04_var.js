/* 
 * 변수: 숫자형, 문자형 확인하기
*/

// 숫자형 
var num1 = 10, num2 = 2;
console.log("num1 숫자출력 => %d", num1);
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);

// 문자형 
var txt1 = "코딩입문", txt2 = "Javascript";
console.log("txt1 문자출력 => %s", txt1);
console.log(txt1 + " / " + txt2);

// 숫자 + 문자 
console.log(num1 + txt2);
console.log(num2 + txt1);


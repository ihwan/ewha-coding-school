/**
 * IF 문 이해하기 
 * TRUE FALSE 확인
 */
if(true){
    console.log("TRUE"); //showing "TRUE"
}

if(false){
    console.log("FALSE"); //not showing
}

/**
 * IF ELSE 문 이해하기
 */
if(true){
    console.log("TRUE");
} else {
    console.log("FALSE");
}

if(false){
    console.log("TRUE");
} else {
    console.log("FALSE");
}

